% setup_variables.m
function model = setup_variables(model)
    comp = model.component('comp1');
    vars = comp.variable.create('var_phys');
    vars.set('rn', 'sqrt((X/GEO_A)^2 + (Y/GEO_B)^2 + 1e-12)');
    vars.set('t_af', 'max(0, min(1, (rn - NP_RATIO)/(1.0 - NP_RATIO + 1e-8)))');
    vars.set('alpha_smooth', '1.0 - (6*t_af^5 - 15*t_af^4 + 10*t_af^3)');
    vars.set('is_np', 'rn < NP_RATIO');
    vars.set('mu_s', 'alpha_smooth*MAT_NP_MU + (1-alpha_smooth)*MAT_AF_MU');
    vars.set('lam_s', 'alpha_smooth*MAT_NP_LAMBDA + (1-alpha_smooth)*MAT_AF_LAMBDA');
    vars.set('wsr', 'alpha_smooth*MAT_NP_WSR + (1-alpha_smooth)*MAT_AF_WSR');
    vars.set('phi0', '1.0 - wsr');
    vars.set('fcd_ref', 'if(is_np, 3.4e-7, 3.4e-7*alpha_smooth + 1.6e-7*(1-alpha_smooth))');
    vars.set('perm_a', 'alpha_smooth*MAT_NP_PERM_A + (1-alpha_smooth)*MAT_AF_PERM_A');
    vars.set('perm_n', 'alpha_smooth*MAT_NP_PERM_N + (1-alpha_smooth)*MAT_AF_PERM_N');
    vars.set('diff_a', 'alpha_smooth*MAT_NP_DIFF_A + (1-alpha_smooth)*MAT_AF_DIFF_A');
    vars.set('diff_b', 'alpha_smooth*MAT_NP_DIFF_B + (1-alpha_smooth)*MAT_AF_DIFF_B');
    vars.set('F11', '1+uX'); vars.set('F12', 'uY'); vars.set('F13', 'uZ');
    vars.set('F21', 'vX'); vars.set('F22', '1+vY'); vars.set('F23', 'vZ');
    vars.set('F31', 'wX'); vars.set('F32', 'wY'); vars.set('F33', '1+wZ');
    vars.set('J', 'abs(F11*(F22*F33-F23*F32) - F12*(F21*F33-F23*F31) + F13*(F21*F32-F22*F31)) + 1e-6');
    vars.set('invJ', 'min(1.0/J, 5.0)');
    vars.set('ws', '(1.0-phi0)*invJ');
    vars.set('wa', 'max(0.05, min(0.99, 1.0-ws))');
    vars.set('B11', 'F11^2+F12^2+F13^2'); vars.set('B22', 'F21^2+F22^2+F23^2'); vars.set('B33', 'F31^2+F32^2+F33^2');
    vars.set('B12', 'F11*F21+F12*F22+F13*F23'); vars.set('B13', 'F11*F31+F12*F32+F13*F33'); vars.set('B23', 'F21*F31+F22*F32+F23*F33');
    vars.set('denom', 'max(J - (1.0-phi0), 0.005)');
    vars.set('p_el', 'lam_s*phi0*(J-1.0)/(denom + 1e-7)');
    vars.set('tm', 'mu_s*invJ');
    vars.set('t_cubic', '3*t_af^2 - 2*t_af^3');
    vars.set('m_af_weight', 't_cubic * (rn > NP_RATIO)');
    vars.set('beta_rad', 'MAT_FIBER_THETA_INNER * pi / 180');
    vars.set('rc_local', 'sqrt(X^2 + Y^2 + 1e-9)');
    vars.set('cb', 'cos(beta_rad)'); vars.set('sb', 'sin(beta_rad)');
    vars.set('etx', '-Y/rc_local'); vars.set('ety', 'X/rc_local');
    vars.set('a0x1', 'cb*etx'); vars.set('a0y1', 'cb*ety'); vars.set('a0z1', 'sb');
    vars.set('nfx1', 'F11*a0x1+F12*a0y1+F13*a0z1'); vars.set('nfy1', 'F21*a0x1+F22*a0y1+F23*a0z1'); vars.set('nfz1', 'F31*a0x1+F32*a0y1+F33*a0z1');
    vars.set('fs1', 'MAT_FIBER_ALPHA_MAX*t_af*0.5*(nfx1^2+nfy1^2+nfz1^2-1.0)*(nfx1^2+nfy1^2+nfz1^2>1.0)*invJ*m_af_weight');
    vars.set('a0x2', '-cb*etx'); vars.set('a0y2', '-cb*ety'); vars.set('a0z2', 'sb');
    vars.set('nfx2', 'F11*a0x2+F12*a0y2+F13*a0z2'); vars.set('nfy2', 'F21*a0x2+F22*a0y2+F23*a0z2'); vars.set('nfz2', 'F31*a0x2+F32*a0y2+F33*a0z2');
    vars.set('fs2', 'MAT_FIBER_ALPHA_MAX*t_af*0.5*(nfx2^2+nfy2^2+nfz2^2-1.0)*(nfx2^2+nfy2^2+nfz2^2>1.0)*invJ*m_af_weight');
    vars.set('sm11', 'tm*(B11-1)+p_el + fs1*nfx1^2 + fs2*nfx2^2');
    vars.set('sm22', 'tm*(B22-1)+p_el + fs1*nfy1^2 + fs2*nfy2^2');
    vars.set('sm33', 'tm*(B33-1)+p_el + fs1*nfz1^2 + fs2*nfz2^2');
    vars.set('sm12', 'tm*B12 + fs1*nfx1*nfy1 + fs2*nfx2*nfy2');
    vars.set('sm13', 'tm*B13 + fs1*nfx1*nfz1 + fs2*nfx2*nfz2');
    vars.set('sm23', 'tm*B23 + fs1*nfy1*nfz1 + fs2*nfy2*nfz2');
    vars.set('cf', 'fcd_ref * phi0 / (wa*J + 1e-12)');
    vars.set('cp', '0.5*(cf + sqrt(cf^2 + 4.0*PHY_C_EXT^2))');
    vars.set('p_osm_cur', 'PHY_R*PHY_T*(2.0*cp - cf - 2.0*PHY_C_EXT)');
    vars.set('p_osm_ref', 'PHY_R*PHY_T*(sqrt(fcd_ref^2+4*PHY_C_EXT^2)-2*PHY_C_EXT)');
    vars.set('p_real', 'mu_w + para_ramp * (p_osm_cur - p_osm_ref)');
    vars.set('T11', 'sm11-p_real'); vars.set('T22', 'sm22-p_real'); vars.set('T33', 'sm33-p_real');
    vars.set('T12', 'sm12'); vars.set('T13', 'sm13'); vars.set('T23', 'sm23');
    vars.set('adj11', 'F22*F33-F23*F32'); vars.set('adj12', 'F13*F32-F12*F33'); vars.set('adj13', 'F12*F23-F13*F22');
    vars.set('adj21', 'F23*F31-F21*F33'); vars.set('adj22', 'F11*F33-F13*F31'); vars.set('adj23', 'F13*F21-F11*F23');
    vars.set('adj31', 'F21*F32-F22*F31'); vars.set('adj32', 'F12*F21-F11*F32'); vars.set('adj33', 'F11*F22-F12*F21');
    vars.set('P11', 'T11*adj11+T12*adj21+T13*adj31'); vars.set('P12', 'T11*adj12+T12*adj22+T13*adj32'); vars.set('P13', 'T11*adj13+T12*adj23+T13*adj33');
    vars.set('P21', 'T12*adj11+T22*adj21+T23*adj31'); vars.set('P22', 'T12*adj12+T22*adj22+T23*adj32'); vars.set('P23', 'T12*adj13+T22*adj23+T23*adj33');
    vars.set('P31', 'T13*adj11+T23*adj21+T33*adj31'); vars.set('P32', 'T13*adj12+T23*adj22+T33*adj32'); vars.set('P33', 'T13*adj13+T23*adj23+T33*adj33');
    vars.set('k_p', 'perm_a * (wa / max(ws, 1e-8))^perm_n');
    vars.set('pore_r', 'sqrt(max(k_p, 1e-16))');
    vars.set('Dp', 'ION_DP0 * exp(-diff_a * min((ION_RP/pore_r)/sqrt(VISCO_SI), 50.0)^diff_b)');
    vars.set('jwx', '-k_p*mu_wX'); vars.set('jwy', '-k_p*mu_wY'); vars.set('jwz', '-k_p*mu_wZ');
    vars.set('jpx', 'cp*jwx-wa*Dp*mu_iX'); vars.set('jpy', 'cp*jwy-wa*Dp*mu_iY'); vars.set('jpz', 'cp*jwz-wa*Dp*mu_iZ');
    vars.set('JwX', 'adj11*jwx+adj12*jwy+adj13*jwz'); vars.set('JwY', 'adj21*jwx+adj22*jwy+adj23*jwz'); vars.set('JwZ', 'adj31*jwx+adj32*jwy+adj33*jwz');
    vars.set('JpX', 'adj11*jpx+adj12*jpy+adj13*jpz'); vars.set('JpY', 'adj21*jpx+adj22*jpy+adj23*jpz'); vars.set('JpZ', 'adj31*jpx+adj32*jpy+adj33*jpz');
end
